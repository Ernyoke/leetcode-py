# https://leetcode.com/problems/3sum-with-multiplicity

import collections
import math
from typing import List


class Solution:
    def threeSumMulti(self, arr: List[int], target: int) -> int:
        counter = collections.Counter()
        for a in arr:
            counter[a] += 1

        ans = 0
        for i, count_i in counter.items():
            for j, count_j in counter.items():
                k = target - i - j
                if k not in counter:
                    continue
                if i == j and j == k:
                    ans += math.comb(count_i, 3)
                if i == j and i != k:
                    ans += math.comb(count_i, 2) * counter[k]
                if i < j < k:
                    ans += count_i * count_j * counter[k]

        return ans % (10 ** 9 + 7)


